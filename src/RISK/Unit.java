package source;

public class Unit {
	private String uClass;
	private int uValue;
	private int uLives;
	
	public void setUClass(String uClass) {
		this.uClass = uClass;
	}
	
	public void setUValue(int uValue) {
		this.uValue = uValue;
	}
	
	public void setULives(int uLives) {
		this.uLives= uLives;
	}
	
	public String getUClass() {
		return uClass;
	}
	public int getUValue() {
		return uValue;
	}
	
	public int getULives() {
		return uLives;
	}
}
